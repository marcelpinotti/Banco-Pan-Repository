package com.aws.lambda.mysql.data;

public class ResponseDetails {
	
	private String mensagemPessoa;
	private String mensagemReacao;
	
	
	public String getMensagemPessoa() {
		return mensagemPessoa;
	}
	public void setMensagemPessoa(String mensagemPessoa) {
		this.mensagemPessoa = mensagemPessoa;
	}
	public String getMensagemReacao() {
		return mensagemReacao;
	}
	public void setMensagemReacao(String mensagemReacao) {
		this.mensagemReacao = mensagemReacao;
	}

}
